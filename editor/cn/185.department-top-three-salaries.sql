SELECT
    B.Name AS 'Department',
    A.Name AS 'Employee',
    A.Salary
FROM
    (
        SELECT
            Name, DepartmentId, Salary,
            DENSE_RANK() OVER(PARTITION BY DepartmentId ORDER BY Salary DESC) AS 'rnk'
        FROM
            Employee
    ) A,
    Department B
WHERE
    A.DepartmentId=B.Id AND
    A.rnk <= 3
